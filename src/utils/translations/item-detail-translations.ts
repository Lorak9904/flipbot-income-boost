const polishPlural = (count: number, one: string, few: string, many: string) => {
  if (count === 1) return one;
  if (count % 10 >= 2 && count % 10 <= 4 && (count % 100 < 12 || count % 100 > 14)) {
    return few;
  }
  return many;
};

const conditionKey = (value: string) => value.trim().toLowerCase().replace(/[\s-]+/g, "_");

const formatCondition = (value: string, labels: Record<string, string>) =>
  labels[conditionKey(value)] || value;

